package com.cnpc.pms.personal.dto;

public class AppDownloadLogDTO {

	private String downloadVersion;
	
	private String downloadtype;
	
	private String curdays;
	
	private String curmonths;
	
	private String totalcounts;
	

	public String getDownloadVersion() {
		return downloadVersion;
	}

	public void setDownloadVersion(String downloadVersion) {
		this.downloadVersion = downloadVersion;
	}

	public String getDownloadtype() {
		return downloadtype;
	}

	public void setDownloadtype(String downloadtype) {
		this.downloadtype = downloadtype;
	}

	public String getCurdays() {
		return curdays;
	}

	public void setCurdays(String curdays) {
		this.curdays = curdays;
	}

	public String getCurmonths() {
		return curmonths;
	}

	public void setCurmonths(String curmonths) {
		this.curmonths = curmonths;
	}

	public String getTotalcounts() {
		return totalcounts;
	}

	public void setTotalcounts(String totalcounts) {
		this.totalcounts = totalcounts;
	}
	
	

	
}
